// This is function for single operation
function sum(a, b) {
    console.log(a + b);
}
sum(10, 15);


// This function for returning parameters
function sum(a, b) {
    return a + b;
}
result = sum(10, 15);
console.log("The sum of these numbers is : ", result);


// This funtion for multipule operation
result1 = sum(5, 10);
result2 = sum(10, 50);
result3 = sum(12, 24);
console.log("The sum of these numbers is : ", result1);
console.log("The sum of these numbers is : ", result2);
console.log("The sum of these numbers is : ", result3);